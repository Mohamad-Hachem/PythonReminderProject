# basic math and arithmetics + - / // * ** %
apples = 0
apples += 5
print(apples)
apples -= 1
print(apples)
apples /= 2
print(apples)
apples *= 2
print(apples)
apples **= 2
print(apples)
apples //= 2
print(apples)
apples += 1
print(apples)
apples %= 3
print(apples)

